function publishedQuestions(category) {
  return category.questions.filter(([, answer]) => answer && answer.trim());
}

function card(category) {
  const button = document.createElement('button');
  button.className = 'category-card';
  const count = publishedQuestions(category).length;
  button.innerHTML = `
    <span class="category-icon">${category.icon}</span>
    <span>
      <h3>${category.title}</h3>
      <p>${category.desc}</p>
      <small>${count} questions</small>
    </span>
    <span class="arrow">→</span>
  `;
  button.onclick = () => showCategory(category.id);
  return button;
}

categories.forEach(category => grid.appendChild(card(category)));

